package vampire.editor.application.startup.configcreator;

public class ConfigImportException extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = -5743830932623515329L;

	ConfigImportException() {
		super();
	}

	ConfigImportException(String message, Throwable cause) {
		super(message, cause);
	}

	ConfigImportException(String message) {
		super(message);
	}

	ConfigImportException(Throwable cause) {
		super(cause);
	}
	
	

}
