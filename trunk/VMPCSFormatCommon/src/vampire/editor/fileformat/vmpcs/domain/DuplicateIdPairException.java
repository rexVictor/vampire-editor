package vampire.editor.fileformat.vmpcs.domain;

public class DuplicateIdPairException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4269198470007829248L;

	public DuplicateIdPairException() {
		super();
	}

	public DuplicateIdPairException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DuplicateIdPairException(String message, Throwable cause) {
		super(message, cause);
	}

	public DuplicateIdPairException(String message) {
		super(message);
	}

	public DuplicateIdPairException(Throwable cause) {
		super(cause);
	}

	
	
}
